package lesson03;

public class Average {

    public int avr(int a, int b, int c, int d, int e, int f, int g, int h, int j, int k){
        return (a + b + c + d + e + f + g + h + j + k) / 10;

    }
    public int rem(int a, int b, int c, int d, int e, int f, int g, int h, int j, int k){
        return (a + b + c + d + e + f + g + h + j + k) % 10;

    }
}
